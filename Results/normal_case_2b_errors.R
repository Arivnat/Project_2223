  g4$Sample<-"200"
  
  df5<-data.frame(df5[i])
  g5<-data.frame(aggregate(df5, list(rep(1:(nrow(df5) %/% n0 + 1), each = n0, len = nrow(df5))), mean)[-1])
  g5$Sample<-"300"
  
