bpc<-function(df1,df2,df3,df4,df5,df6,i){
  df1<-data.frame(df1[i])
  n0<-nrow(df1)/1000
  g1<-data.frame(aggregate(df1, list(rep(1:(nrow(df1) %/% n0 + 1), each = n0, len = nrow(df1))), mean)[-1])
  g1$Sample<-"50"
  
  df2<-data.frame(df2[i])
  g2<-data.frame(aggregate(df2, list(rep(1:(nrow(df2) %/% n0 + 1), each = n0, len = nrow(df2))), mean)[-1])
  g2$Sample<-"70"
  
