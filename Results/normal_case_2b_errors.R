  g6<-data.frame(aggregate(df6, list(rep(1:(nrow(df6) %/% n0 + 1), each = n0, len = nrow(df6))), mean)[-1])
  g6$Sample<-"500"
  
  names(g1)<-names(g2)<-names(g3)<-names(g4)<-names(g5)<-names(g6)<-c("DCP-QR","DCP-opt",
                                                                      "CQR","CQR-m","CQR-r","CP-reg","Sample")
  
 
  dat<-rbind.data.frame(g1,g2,g3,g4,g5,g6)
  dat$Sample<-factor(dat$Sample,levels=c("50","70","100","200","300","500"))
  datm<-melt(dat,id.vars="Sample")
  
  p1<-ggplot(datm,aes(x=Sample,y=value,col=variable))+
   facet_wrap(~variable)+
    geom_boxplot()+
   stat_summary(fun = mean, geom = "point", col = "red") +  
  stat_summary(fun = mean, geom = "text", col = "darkred",     
